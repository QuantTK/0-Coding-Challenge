def get_vowels(string):
    for i in string:
        if i == "A":
            print("A")
        elif i =="a":
            print("a")
        elif i == "E":
            print("E")
        elif i == "e":
            print("e")
        elif i == "I":
            print("i")
        elif i == "O":
            print("O")
        elif i == "o":
            print("o")
        elif i == "U":
            print("U")
        elif i == "u":
            print("u")

get_vowels("TlhAkodIso")